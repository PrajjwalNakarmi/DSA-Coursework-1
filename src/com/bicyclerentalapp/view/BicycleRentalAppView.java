    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        // Get the Buyer ID from the input field
        String customerId = txtfldcust.getText().trim();

        // Validate the Buyer ID input
        if (customerId.isEmpty() || !isNumeric(customerId)) {
            JOptionPane.showMessageDialog(this, "Please enter a valid Buyer ID to remove.");
            return;
        }

        int customer = Integer.parseInt(customerId);
        BicycleRentalModel customerRemove = null;

        // Search for the buyer in the list
        for (BicycleRentalModel customerID : custList) {
            if (customerID.getCustomerId() == customer) {
                customerRemove = customerID;
                break;
            }
        }

        // If the buyer is found, remove them
        if (customerRemove != null) {
           custList.remove(customerRemove);

            // Find the row index in the table model and remove it
            int rowIndex = -1;
            for (int i = 0; i < bicycleRentalTableModel.getRowCount(); i++) {
                if ((int) bicycleRentalTableModel.getValueAt(i, 0) == customer) { 
                    rowIndex = i;
                    break;
                }
            }

            if (rowIndex != -1) {
                bicycleRentalTableModel.removeRow(rowIndex);
            }

            JOptionPane.showMessageDialog(this, "Buyer removed successfully.");
            resetFields(); // Clear input fields after removing
        } else {
            JOptionPane.showMessageDialog(this, "Buyer ID not found.");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

