    private void addTableContent(int customerId, String fullname, String email, String contact, int rentedDays, String modelNo, String modelName){
        try{
            BicycleRentalModel newCustomer;
            newCustomer = new BicycleRentalModel(customerId, fullname, email, Integer.parseInt(contact), rentedDays, modelNo, modelName);
            
            // Add the buyer to the list
            custList.add(newCustomer);
            
            // Add the buyer's data to the table model
            bicycleRentalTableModel.addRow(new Object[]{
                newCustomer.getCustomerId(),
                newCustomer.getFullname(),
                newCustomer.getEmail(),
                newCustomer.getContact(),
                newCustomer.getRentedDays(),
                newCustomer.getModelNo(),
                newCustomer.getModelName()
            });
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "An error occurred while adding the buyer. Please check the input values.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    // Helper method to check if a string is numeric
    private boolean isNumeric(String str) {
        try {
            Long.valueOf(str); // Use Long to allow larger numbers
            return true;
        } catch (NumberFormatException e) {
            return false;
        }
    }
    
    private boolean validation() {
        
        String CustomerID = txtfldcust.getText().trim();
        if (CustomerID.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Customer ID cannot be empty.");
            return false;
        }

        // Validate Full Name
        String fullName = txtfldName.getText().trim();
        if (fullName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Full Name cannot be empty.");
            return false;
        }

        
        String email = txtfldEmail.getText().trim();
        if (email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Email cannot be empty.");
            return false;
        }

        
        String contact = txtfldContact.getText().trim();
        if (contact.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Contact No cannot be empty.");
            return false;
        }

        
        String modelNo = txtfldModelNo.getText().trim();
        if (modelNo.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Model No cannot be empty.");
            return false;
        }

        
        String modelName = txtfldModelName.getText().trim();
        if (modelName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Model Name cannot be empty.");
            return false;
        }

        
        if (!isNumeric(CustomerID)) {
            JOptionPane.showMessageDialog(this, "Customer ID must be a numeric value.");
            return false;
        }

        int customerId = Integer.parseInt(CustomerID);
        // Check if Buyer ID already exists
        for (BicycleRentalModel buyer : custList) {
            if (buyer.getCustomerId() == customerId) {
                JOptionPane.showMessageDialog(this, "Buyer ID already exists.");
                return false;
            }
        }

        // Validate Contact No length and numeric
        if (!isNumeric(contact) || contact.length() != 10) {
            JOptionPane.showMessageDialog(this, "Contact No must be a 10-digit number and cannot contain letters.");
            return false;
        }

        return true; // All validations passed
    }
    
